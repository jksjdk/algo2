    def __init__(self,name):
        self.nombre = name 
        
    def dormir(self):
        print("estoy durmiendo", self.nombre)
        
    def hablar(self,hablar):
        print("estoy hablando",hablar)
     
    def cantar(self):
        print("estoy cantando",self.nombre)
        
        
